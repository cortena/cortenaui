package com.cortena.ui.spacing

/**
 * CortenaUI — Spacing
 *
 * 4dp base grid. All spacing in the design system is a multiple of 4. Values are Float (dp
 * equivalent). Framework-agnostic.
 */
object Spacing {
    const val None: Float = 0f
    const val Xs: Float = 4f
    const val Sm: Float = 8f
    const val Md: Float = 16f
    const val Lg: Float = 24f
    const val Xl: Float = 32f
    const val Xxl: Float = 48f
    const val Xxxl: Float = 64f
}
